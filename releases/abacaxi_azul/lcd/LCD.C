//@@@@@@@@@@@@@@@@@@@@ CHARACTER DEGREE @@@@@@@@@@@@@@@@@//
void CustomChar_graus(char pos_row, char pos_char)       //   print new char in LCD              //
{
 char i;
 Lcd_Cmd(64);
 for (i=0; i<=10; i++)
    Lcd_Chr_CP(character_graus[i]);
 Lcd_Cmd(_LCD_RETURN_HOME);
 Lcd_Chr(pos_row, pos_char, 0);
}




//@@@@@@@@@@@@@@@@@@@@@@@ CLEAR LCD @@@@@@@@@@@@@@@@@@@@@//
void ClearLcd()										     //   my self function to LCD CLEANER   //   
{
 Lcd_Cmd(_LCD_CLEAR);
 Lcd_Cmd(_LCD_CURSOR_OFF);
 delay_ms(50);
 }

